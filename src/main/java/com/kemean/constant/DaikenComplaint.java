package com.kemean.constant;

/**
 * 投诉
 * 
 * @Date 2018年7月23日
 *
 * @company 深圳科名网络有限公司 {@link www.kemean.com}
 */
public class DaikenComplaint {

	/**
	 * 投诉类型
	 * 
	 * @Date 2018年7月23日
	 *
	 * @company 深圳科名网络有限公司 {@link www.kemean.com}
	 */
	public enum ComplaintType {
		/** 店铺 **/
		SHOP(1101),
		/** 调研 **/
		INVEST(1201),
		/** 销售 **/
		SALE(1301),
		/** 平台管理 **/
		ADMIN(1401);
		private Integer type;

		private ComplaintType(Integer type) {
			this.type = type;
		}

		public Integer getType() {
			return type;
		}

		public void setType(Integer type) {
			this.type = type;
		}
	}

	/**
	 * 投诉状态
	 * 
	 * @Date 2018年7月23日
	 *
	 * @company 深圳科名网络有限公司 {@link www.kemean.com}
	 */
	public enum ComplaintStatus {
		/** 待处理 **/
		WAIT_DEAL(1101),
		/** 处理中 **/
		DEAL_IN(1201),
		/** 已处理 **/
		FINISH(1301);

		private Integer status;

		private ComplaintStatus(Integer status) {
			this.status = status;
		}

		public Integer getStatus() {
			return status;
		}

		public void setStatus(Integer status) {
			this.status = status;
		}

	}
}
