package com.kemean.vo.po.admin;

import javax.validation.constraints.NotNull;

public class AdminMonitorPO {
	@NotNull(message = "objId不能为空")
	private Integer objId;

	@NotNull(message = "status不能为空")
	private Boolean status;

	// true 操作推荐,false 操作上下架状态，
	@NotNull(message = "监控的状态不能为空")
	private Boolean tjOperate;

	public Integer getObjId() {
		return objId;
	}

	public void setObjId(Integer objId) {
		this.objId = objId;
	}

	public Boolean getStatus() {
		return status;
	}

	public void setStatus(Boolean status) {
		this.status = status;
	}

	public Boolean getTjOperate() {
		return tjOperate;
	}

	public void setTjOperate(Boolean tjOperate) {
		this.tjOperate = tjOperate;
	}

}
