package com.kemean.vo.bo.admin.treasure;

import com.kemean.vo.bo.KemeanIdBO;

public class TreasureDataBO extends KemeanIdBO {

	/**
	 * 商品信息
	 */
	private String goodsTitle;

	/**
	 * 屏位
	 */
	private String screenPositionStr;

	/**
	 * 手机号
	 */
	private String userPhone;

	/**
	 * 购买点击量
	 */
	private String buyClickNumStr;

	/**
	 * 已点击量
	 */
	private Integer clickNum;

	/**
	 * 当前状态
	 */
	private String currentStateStr;

	/**
	 * 上下架状态
	 */
	private Boolean status;

	public String getGoodsTitle() {
		return goodsTitle;
	}

	public void setGoodsTitle(String goodsTitle) {
		this.goodsTitle = goodsTitle;
	}

	public String getScreenPositionStr() {
		return screenPositionStr;
	}

	public void setScreenPositionStr(String screenPositionStr) {
		this.screenPositionStr = screenPositionStr;
	}

	public String getUserPhone() {
		return userPhone;
	}

	public void setUserPhone(String userPhone) {
		this.userPhone = userPhone;
	}

	public String getBuyClickNumStr() {
		return buyClickNumStr;
	}

	public void setBuyClickNumStr(String buyClickNumStr) {
		this.buyClickNumStr = buyClickNumStr;
	}

	public Integer getClickNum() {
		return clickNum;
	}

	public void setClickNum(Integer clickNum) {
		this.clickNum = clickNum;
	}

	public String getCurrentStateStr() {
		return currentStateStr;
	}

	public void setCurrentStateStr(String currentStateStr) {
		this.currentStateStr = currentStateStr;
	}

	public Boolean getStatus() {
		return status;
	}

	public void setStatus(Boolean status) {
		this.status = status;
	}

}
