package com.kemean.vo.bo.admin;

public class AdminChartBO {

	private Double y;
	private String name;

	public AdminChartBO() {
		super();
	}

	public AdminChartBO(Double y, String name) {
		super();
		this.y = y;
		this.name = name;
	}

	public Double getY() {
		return y;
	}

	public void setY(Double y) {
		this.y = y;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
