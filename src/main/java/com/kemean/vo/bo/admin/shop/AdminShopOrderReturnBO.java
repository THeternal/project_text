package com.kemean.vo.bo.admin.shop;

import java.util.Date;

import com.fasterxml.jackson.annotation.JsonFormat;
import com.kemean.vo.bo.KemeanIdBO;

public class AdminShopOrderReturnBO extends KemeanIdBO {
	
	/**
	 * 订单编号
	 */
	private String orderNo;
	
	/**
	 * 商品编号
	 */
	private String goodsUid;

	/**
	 * 商品名称
	 */
	private String goodsTitles;
	
	/**
	 * 用户手机号
	 */
	private String userPhone;
	
	/**
	 * 订单金额
	 */
	private Double pricePay;
	
	/**
	 * 补偿金额
	 */
	private Double compensateMoney;
	
	/**
	 * 补偿时间
	 */
	@JsonFormat(pattern = "yyyy-MM-dd HH:mm", timezone = "GMT+8")
	private Date compensateTime;
	
	/**
	 * 购买用户名称
	 */
	private String userName;
	
	/**
	 * 商品购买量
	 */
	private Integer quantity;

	/**
	 * 退款件数
	 */
	private Integer goodsNum;

	/**
	 * 退款原因
	 */
	private String refundReason;

	/**
	 * 退款金额
	 */
	private Double refundMoney;
	
	/**
	 * 商品状态（true-收到货物，false-没有收到）
	 */
	private Boolean goodsStatus;
	
	/**
	 * 退款状态 - 处理状态
	 */
	private Integer refundStatus;
	
	/**
	 * 快递单号
	 */
	private String expressWaybillNo;

	/**
	 * 订单时间
	 */
	@JsonFormat(pattern = "yyyy-MM-dd HH:mm", timezone = "GMT+8")
	private Date orderTime;
	
	/**
	 * 退货时间
	 */
	@JsonFormat(pattern = "yyyy-MM-dd HH:mm", timezone = "GMT+8")
	private Date refundTime;

	public String getOrderNo() {
		return orderNo;
	}

	public void setOrderNo(String orderNo) {
		this.orderNo = orderNo;
	}

	public String getGoodsUid() {
		return goodsUid;
	}

	public void setGoodsUid(String goodsUid) {
		this.goodsUid = goodsUid;
	}

	public String getGoodsTitles() {
		return goodsTitles;
	}

	public void setGoodsTitles(String goodsTitles) {
		this.goodsTitles = goodsTitles;
	}

	public String getUserPhone() {
		return userPhone;
	}

	public void setUserPhone(String userPhone) {
		this.userPhone = userPhone;
	}

	public String getUserName() {
		return userName;
	}

	public void setUserName(String userName) {
		this.userName = userName;
	}

	public Integer getQuantity() {
		return quantity;
	}

	public void setQuantity(Integer quantity) {
		this.quantity = quantity;
	}

	public Integer getGoodsNum() {
		return goodsNum;
	}

	public void setGoodsNum(Integer goodsNum) {
		this.goodsNum = goodsNum;
	}

	public String getRefundReason() {
		return refundReason;
	}

	public void setRefundReason(String refundReason) {
		this.refundReason = refundReason;
	}

	public Double getRefundMoney() {
		return refundMoney;
	}

	public void setRefundMoney(Double refundMoney) {
		this.refundMoney = refundMoney;
	}

	public Boolean getGoodsStatus() {
		return goodsStatus;
	}

	public void setGoodsStatus(Boolean goodsStatus) {
		this.goodsStatus = goodsStatus;
	}

	public Integer getRefundStatus() {
		return refundStatus;
	}

	public void setRefundStatus(Integer refundStatus) {
		this.refundStatus = refundStatus;
	}

	public String getExpressWaybillNo() {
		return expressWaybillNo;
	}

	public void setExpressWaybillNo(String expressWaybillNo) {
		this.expressWaybillNo = expressWaybillNo;
	}

	public Date getOrderTime() {
		return orderTime;
	}

	public void setOrderTime(Date orderTime) {
		this.orderTime = orderTime;
	}

	public Date getRefundTime() {
		return refundTime;
	}

	public void setRefundTime(Date refundTime) {
		this.refundTime = refundTime;
	}

	public Double getPricePay() {
		return pricePay;
	}

	public void setPricePay(Double pricePay) {
		this.pricePay = pricePay;
	}

	public Double getCompensateMoney() {
		return compensateMoney;
	}

	public void setCompensateMoney(Double compensateMoney) {
		this.compensateMoney = compensateMoney;
	}

	public Date getCompensateTime() {
		return compensateTime;
	}

	public void setCompensateTime(Date compensateTime) {
		this.compensateTime = compensateTime;
	}

}
