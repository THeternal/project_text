package com.kemean.vo.bo.admin.user;

public class KemeanAdminUserRecord {

	private String wxNun;

	private String phone;

	private String email;

	public KemeanAdminUserRecord() {
		super();
	}

	public KemeanAdminUserRecord(String wxNun, String phone, String email) {
		super();
		this.wxNun = wxNun;
		this.phone = phone;
		this.email = email;
	}

	public String getWxNun() {
		return wxNun;
	}

	public void setWxNun(String wxNun) {
		this.wxNun = wxNun;
	}

	public String getPhone() {
		return phone;
	}

	public void setPhone(String phone) {
		this.phone = phone;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}
}
