package com.kemean.bean;

import javax.persistence.*;

@Table(name = "daiken_investigate_options")
public class DaikenInvestigateOptions extends KemeanAbstractBaseBean {

	/**
	 * 调研表id
	 */
	@Column(name = "investigate_id")
	private Integer investigateId;

	/**
	 * 选手名称
	 */
	@Column(name = "name")
	private String name;

	/**
	 * 选手图片
	 */
	@Column(name = "img")
	private String img;

	/**
	 * 选手说明
	 */
	@Column(name = "explain_str")
	private String explainStr;

	/**
	 * 获得票数
	 */
	@Column(name = "votes")
	private Integer votes;

	/**
	 * 人气
	 */
	@Column(name = "popularity")
	private Integer popularity;

	public Integer getPopularity() {
		return popularity;
	}

	public void setPopularity(Integer popularity) {
		this.popularity = popularity;
	}

	public Integer getInvestigateId() {
		return investigateId;
	}

	public void setInvestigateId(Integer investigateId) {
		this.investigateId = investigateId;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getImg() {
		return img;
	}

	public void setImg(String img) {
		this.img = img;
	}

	public String getExplainStr() {
		return explainStr;
	}

	public void setExplainStr(String explainStr) {
		this.explainStr = explainStr;
	}

	public Integer getVotes() {
		return votes;
	}

	public void setVotes(Integer votes) {
		this.votes = votes;
	}

}