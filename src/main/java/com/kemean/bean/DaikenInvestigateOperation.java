package com.kemean.bean;

import javax.persistence.*;

@Table(name = "daiken_investigate_operation")
public class DaikenInvestigateOperation extends KemeanAbstractBaseBean {

	/**
	 * 调研id
	 */
	@Column(name = "investigate_id")
	private Integer investigateId;

	/**
	 * 发布类型
	 */
	@Column(name = "type")
	private Integer type;

	/**
	 * 用户id
	 */
	@Column(name = "user_id")
	private Integer userId;

	/**
	 * 问题id
	 */
	@Column(name = "question_id")
	private Integer questionId;

	/**
	 * 做了何种操作
	 */
	@Column(name = "operation")
	private String operation;

	public Integer getType() {
		return type;
	}

	public void setType(Integer type) {
		this.type = type;
	}

	public Integer getInvestigateId() {
		return investigateId;
	}

	public void setInvestigateId(Integer investigateId) {
		this.investigateId = investigateId;
	}

	public Integer getUserId() {
		return userId;
	}

	public void setUserId(Integer userId) {
		this.userId = userId;
	}

	public String getOperation() {
		return operation;
	}

	public void setOperation(String operation) {
		this.operation = operation;
	}

	public Integer getQuestionId() {
		return questionId;
	}

	public void setQuestionId(Integer questionId) {
		this.questionId = questionId;
	}

}